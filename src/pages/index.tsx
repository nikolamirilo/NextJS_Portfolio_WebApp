import { Card, Hero } from "@/components";
import { Project } from "@/types/api_responses";
import Head from "next/head";
import React from "react";

export async function getStaticProps() {
  const response = await fetch("https://mirilo-nikola.netlify.app/api/projects");
  const data = await response.json();

  return {
    props: {
      projects: data.projects,
    },
  };
}

interface HomeProps {
  projects: Project[];
}

const Home: React.FC<HomeProps> = ({ projects }) => {
  return (
    <main className="home">
      <Head>
        <title>Nikola Mirilo</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/NM.png" />
      </Head>
      <Hero
        title="Empowering IT projects to success"
        subtitle="Utilizing my skills in project management and programming to drive successful results by effective planning and executing projects, delivering on deadlines and goals, and developing customized solutions through programming."
        image="/images/office.png"
      />
      <div id="portfolio">
        <h1>Portfolio</h1>
        <div className="cards-container" scroll={false}>
          {projects
            ? projects.map((project, idx) => {
                return (
                  <Card
                    key={idx}
                    id={project.id}
                    title={project.title}
                    image={project.image}
                    subtitle="This is my project, I worked on this project"
                  />
                );
              })
            : null}
        </div>
      </div>
    </main>
  );
};
export default Home;
